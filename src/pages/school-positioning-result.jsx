function pickLocale(loc) {
  return loc === 'en' ? 'en' : 'zh-Hans';
}

function getLabel(node, locale) {
  if (!node) return '';
  if (typeof node === 'string') return node;
  return node[locale] || node['zh-Hans'] || node.en || '';
}

function SchoolCard({ school, locale, t }) {
  const name = getLabel(school.school || school.name, locale);
  const program = getLabel(school.program, locale);
  const reason = getLabel(school.reason, locale);
  const rawHref = school.doc || school.slug || school.url || school.link;
  const href = rawHref ? encodeURI(rawHref) : null;
  return (
    <div className={styles.schoolCard}>
      <h4 className={styles.schoolName}>{name || '—'}</h4>
      {program && <p className={styles.schoolProgram}>{program}</p>}
      {reason && <p className={styles.schoolReason}>{reason}</p>}
      {href && (
        <a className={styles.schoolLink} href={href}>
          {t.viewDetail} &rarr;
        </a>
      )}
    </div>
  );
}

function Bucket({ items, title, subtitle, variantClass, locale, t }) {
  const list = Array.isArray(items) ? items : [];
  return (
    <div className={`${styles.bucket} ${variantClass}`}>
      <div className={styles.bucketHeader}>
        <h3 className={styles.bucketTitle}>
          {title}
          <span className={styles.bucketSubtitle}>{subtitle}</span>
        </h3>
        <span className={styles.bucketCount}>{list.length}</span>
      </div>
      <div className={styles.schoolList}>
        {list.length === 0 ? (
          <div className={styles.emptyBucket}>{t.emptyBucket}</div>
        ) : (
          list.map((s, i) => (
            <SchoolCard key={s.id || s.slug || i} school={s} locale={locale} t={t} />
          ))
        )}
      </div>
    </div>
  );
}

function ResultBody() {
  const { i18n } = useDocusaurusContext();
  const locale = pickLocale(i18n.currentLocale);
  const t = COPY[locale];

  const [sessionId, setSessionId] = useState(null);
  const [status, setStatus] = useState('init');
  const [data, setData] = useState(null);
  const [errorMsg, setErrorMsg] = useState('');
  const [attempt, setAttempt] = useState(0);
  const [downloading, setDownloading] = useState(false);
  const timerRef = useRef(null);
  const cancelledRef = useRef(false);
  const reportRef = useRef(null);

  const handleDownloadPdf = async () => {
    if (!reportRef.current || downloading) return;
    setDownloading(true);
    try {
      const mod = await import('html2pdf.js');
      const html2pdf = mod.default || mod;
      const tierName = data && data.tier ? data.tier : '';
      const fileName = `csgrad-positioning-${tierName || 'report'}-${new Date().toISOString().slice(0, 10)}.pdf`;
      await html2pdf()
        .set({
          margin: [12, 10, 12, 10],
          filename: fileName,
          image: { type: 'jpeg', quality: 0.95 },
          html2canvas: { scale: 2, useCORS: true, backgroundColor: '#ffffff' },
          jsPDF: { unit: 'mm', format: 'a4', orientation: 'portrait' },
          pagebreak: { mode: ['css', 'legacy'] },
        })
        .from(reportRef.current)
        .save();
    } finally {
      setDownloading(false);
    }
  };

  useEffect(() => {
    const params = new URLSearchParams(window.location.search);
    const sid = params.get('session_id') || params.get('sessionId');
    if (!sid) {
      setStatus('missing');
      return;
    }
    setSessionId(sid);
  }, []);

  useEffect(() => {
    if (!sessionId) return;
    cancelledRef.current = false;
    let count = 0;

    const poll = async () => {
      if (cancelledRef.current) return;
      count += 1;
      setAttempt(count);
      try {
        const url = WORKER_BASE_URL + '/api/positioning/result?sessionId=' + encodeURIComponent(sessionId);
        const res = await fetch(url);
        if (!res.ok) {
          const text = await res.text().catch(() => '');
          throw new Error(text || `HTTP ${res.status}`);
        }
        const body = await res.json();
        if (cancelledRef.current) return;
        if (body && body.status === 'paid') {
          setData(body);
          setStatus('paid');
          return;
        }
        if (count >= MAX_POLLS) {
          setStatus('timeout');
          return;
        }
        setStatus('pending');
        timerRef.current = setTimeout(poll, POLL_INTERVAL_MS);
      } catch (err) {
        if (cancelledRef.current) return;
        if (count >= MAX_POLLS) {
          setErrorMsg(err && err.message ? err.message : String(err));
          setStatus('error');
          return;
        }
        timerRef.current = setTimeout(poll, POLL_INTERVAL_MS);
      }
    };

    poll();
    return () => {
      cancelledRef.current = true;
      if (timerRef.current) clearTimeout(timerRef.current);
    };
  }, [sessionId]);

  const retry = () => {
    setStatus('init');
    setErrorMsg('');
    setData(null);
    setAttempt(0);
    if (sessionId) {
      setSessionId(null);
      setTimeout(() => setSessionId(sessionId), 0);
    }
  };

  const formHref = locale === 'en' ? '/en/school-positioning' : '/school-positioning';
  const homeHref = locale === 'en' ? '/en/' : '/';

  if (status === 'missing') {
    return (
      <div className={styles.pageWrapper}>
        <div className={styles.container}>
          <a href={formHref} className={styles.backLink}>&larr; {t.backForm}</a>
          <div className={styles.statusCard}>
            <h2 className={styles.statusTitle}>{t.missingTitle}</h2>
            <p className={styles.statusText}>{t.missingText}</p>
          </div>
        </div>
      </div>
    );
  }

  if (status === 'paid' && data) {
    const list = data.schoolList || {};
    const tierName = data.tier || '';
    const summary = getLabel(list.summary, locale);
    return (
      <div className={styles.pageWrapper}>
        <div className={styles.container}>
          <div className={styles.topBar}>
            <a href={homeHref} className={styles.backLink}>&larr; {t.backHome}</a>
            <button
              type="button"
              className={styles.downloadBtn}
              onClick={handleDownloadPdf}
              disabled={downloading}
            >
              {downloading ? t.downloadingPdf : t.downloadPdf}
            </button>
          </div>
          <div ref={reportRef} className={styles.report}>
            <div className={styles.reportHeader}>
              <p className={styles.reportTitle}>{t.reportTitle}</p>
              <p className={styles.reportDate}>{new Date().toISOString().slice(0, 10)}</p>
            </div>
            <div className={styles.tierHeader}>
              <p className={styles.tierLabel}>{t.tierLabel}</p>
              <h1 className={styles.tierName}>{tierName}</h1>
              {summary && <p className={styles.summary}>{summary}</p>}
            </div>
            {Array.isArray(data.warnings) && data.warnings.length > 0 && (
              <div className={styles.warningList}>
                {data.warnings.map((w, i) => (
                  <div
                    key={i}
                    className={`${styles.warningItem} ${w.severity === 'high' ? styles.warningHigh : styles.warningInfo}`}
                  >
                    {w.message}
                  </div>
                ))}
              </div>
            )}
            <div className={styles.bucketsGrid}>
              <Bucket
                items={list.reach}
                title={t.bucketReach}
                subtitle={t.bucketReachSub}
                variantClass={styles.bucketReach}
                locale={locale}
                t={t}
              />
              <Bucket
                items={list.match}
                title={t.bucketMatch}
                subtitle={t.bucketMatchSub}
                variantClass={styles.bucketMatch}
                locale={locale}
                t={t}
              />
              <Bucket
                items={list.safety}
                title={t.bucketSafety}
                subtitle={t.bucketSafetySub}
                variantClass={styles.bucketSafety}
                locale={locale}
                t={t}
              />
            </div>
            {data.phdRecommended && Array.isArray(data.phdRecommended.schools) && data.phdRecommended.schools.length > 0 && (
              <div className={styles.phdSection}>
                <div className={styles.phdHeader}>
                  <h2 className={styles.phdTitle}>
                    {t.phdSectionTitle}
                    <span className={styles.phdSubtitle}>{t.phdSectionSub}</span>
                  </h2>
                </div>
                {data.phdRecommended.summary && (
                  <p className={styles.phdSummary}>{getLabel(data.phdRecommended.summary, locale)}</p>
                )}
                <div className={styles.phdList}>
                  {data.phdRecommended.schools.map((s, i) => (
                    <SchoolCard key={'phd-' + (s.school || i)} school={s} locale={locale} t={t} />
                  ))}
                </div>
              </div>
            )}
            {data.codingTransitionRecommended && Array.isArray(data.codingTransitionRecommended.schools) && data.codingTransitionRecommended.schools.length > 0 && (
              <div className={styles.codingSection}>
                <div className={styles.codingHeader}>
                  <h2 className={styles.codingTitle}>
                    {t.codingSectionTitle}
                    <span className={styles.codingSubtitle}>{t.codingSectionSub}</span>
                  </h2>
                </div>
                {data.codingTransitionRecommended.summary && (
                  <p className={styles.codingSummary}>{getLabel(data.codingTransitionRecommended.summary, locale)}</p>
                )}
                <div className={styles.codingList}>
                  {data.codingTransitionRecommended.schools.map((s, i) => (
                    <SchoolCard key={'coding-' + (s.school || i)} school={s} locale={locale} t={t} />
                  ))}
                </div>
              </div>
            )}
            <div className={styles.reportFooter}>{t.reportFooter}</div>
          </div>
        </div>
      </div>
    );
  }

  if (status === 'error') {
    return (
      <div className={styles.pageWrapper}>
        <div className={styles.container}>
          <a href={formHref} className={styles.backLink}>&larr; {t.backForm}</a>
          <div className={styles.statusCard}>
            <h2 className={styles.statusTitle}>{t.errorTitle}</h2>
            <p className={styles.statusText}>{errorMsg}</p>
            <button className={styles.retryBtn} onClick={retry}>{t.retry}</button>
          </div>
        </div>
      </div>
    );
  }

  if (status === 'timeout') {
    return (
      <div className={styles.pageWrapper}>
        <div className={styles.container}>
          <a href={formHref} className={styles.backLink}>&larr; {t.backForm}</a>
          <div className={styles.statusCard}>
            <h2 className={styles.statusTitle}>{t.timeoutTitle}</h2>
            <p className={styles.statusText}>{t.timeoutText}</p>
            <button className={styles.retryBtn} onClick={retry}>{t.retry}</button>
          </div>
        </div>
      </div>
    );
  }

  return (
    <div className={styles.pageWrapper}>
      <div className={styles.container}>
        <a href={formHref} className={styles.backLink}>&larr; {t.backForm}</a>
        <div className={styles.statusCard}>
          <div className={styles.spinner} />
          <h2 className={styles.statusTitle}>{t.pendingTitle}</h2>
          <p className={styles.statusText}>
            {t.pendingText}
            {attempt > 0 ? ` (${attempt}/${MAX_POLLS})` : ''}
          </p>
        </div>
      </div>
    </div>
  );
}

export default function SchoolPositioningResultPage() {
  const { i18n } = useDocusaurusContext();
  const locale = pickLocale(i18n.currentLocale);
  const t = COPY[locale];
  return (
    <Layout title={t.pageTitle} description={t.pageDesc}>
      <Head>
        <meta name="description" content={t.pageDesc} />
        <meta property="og:title" content={t.pageTitle} />
        <meta property="og:description" content={t.pageDesc} />
        <meta property="og:type" content="website" />
      </Head>
      <BrowserOnly fallback={<div className={styles.pageWrapper} />}>
        {() => <ResultBody />}
      </BrowserOnly>
    </Layout>
  );
}
